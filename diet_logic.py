def diet_plan(category):

    if category == "Underweight":
        return "Increase calories: milk, banana, rice, peanut butter, eggs"

    elif category == "Normal":
        return "Balanced diet: chapati, dal, vegetables, fruits, curd"

    elif category == "Overweight":
        return "Low calorie: oats, salad, fruits, avoid fried foods"

    elif category == "Obese":
        return "Strict diet: boiled vegetables, green tea, avoid sugar & junk food"